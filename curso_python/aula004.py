# tipos int e float
# int -> número inteiro
# o tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado positivo

print(11) # int
print(-11) # int
print(0)


# float -> número com ponto flutuante 
# o tipo float representa qualquer número positivo ou negativo
# float sem sinal é considerado positivo 

print(1.1, 10.11) # para separar números SEMPRE se usa um ponto 
print(0.0) # float
print(-1.5) # float 

# a função type mostra o tipo que o python inferiu ao valor
print(type('Hello, World')) # aqui ao invés de imprimir minha string, vai mostrar que tipo de dado ela é
print(type(123))
print(type(1.1))
