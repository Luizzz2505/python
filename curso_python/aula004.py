'''''''''
Tipos int e float

int -> números inteiros
float -> números com vírgula

o int representa qualquer número, seja ele negativo ou positivo
o float representa qualquer número com ponto flutuante, ou seja, números quebrados (vírgula)

'''''''''

print(11) # int
print(-11) # int
print(0)
print(1.1, 10.11) # float
print(0.0, -1.5)

# A função type mostra o tipo de que Python inferiu ao valor
print(type('Luiz'))
print(type(10))
print(type(10.12))


