# tipo de dado bool (boolean)
# ao questionar algo em um programa, só existem duas respostas possíveis: sim (True) ou não (False)
# existem vários operadores para "questionar"
# Dentre eles o ==, que é um operador lógico que  questiona se um valor é igual ao outro

print(10 == 10) # sim -> True (verdadeiro)
print(10 == 1) # não -> False (falso)
print(type(10 == 10))
print(type(10 == 11))