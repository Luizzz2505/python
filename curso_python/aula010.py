concatenacao = 'A' + 'B' + 'C'
print(concatenacao) # juntou todos as strings 
# o + faz a adição, mas dependendo do tipo ele faz a concatenação (polimorfismo)

# o * além da multiplicação, também serve para fazer a repetição
a_dez_vezes = 'A' * 10
print(a_dez_vezes)
tres_vezes_luiz = 3 * 'Luiz'
print(tres_vezes_luiz)
