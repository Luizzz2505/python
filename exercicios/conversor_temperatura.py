# Crie um programa que converta a temperatura de Celsius para Fahrenheit. Solicite a temperatura em Celsius do usuário e exiba o resultado usando f-strings.

temperatura_c = float(input('Informe a temperatura me graus Celsius: '))
temperatura_f = 9/5 * temperatura_c + 32

print(f'A temperatua que você informou em Celsius é: {temperatura_f} em Fahrenheit ')

